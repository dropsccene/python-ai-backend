# 1. GET 为什么应该是幂等且安全的？如果 GET 修改了数据会怎样？
# - 因为GET 只是进行数据的请求，不会改变服务器里的数据。我认为：如果 GET 修改了数据，可能会导致该请求拒绝通过。
# - 实际上：GET 请求可能被任何人、任何中间件、任何爬虫随时发送，如果它修改了数据，你都不知道是谁删的。

# 2. POST vs PUT 的本质区别是什么？（提示：幂等性）
# - 我认为：POST 用于创建资源，而PUT用于更新资源。所以POST 不是幂等的，而PUT 是幂等的。
# - 实际上：PUT 也可以进行创建。比如：PUT /users/123 可以创建一个用户，如果用户不存在的话。如果存在，就覆盖它。这才是幂等的本质：“不管资源存不存在，我把这个 URI 的内容设置成这个值”。
#          POST 的真正特征是：“我往这个集合里加东西，生成什么 URI 由服务器决定”。所以 POST 不是幂等的 -- 发两次就创建两条。

# 3. PATCH 是什么？你的 CRUD 里为什么用 PUT 而不是 PATCH？
# - PATCH 是用于部分更新资源的一种方法，而PUT是用于完全替代资源的方法。在 CRUD 中，使用 PUT 是因为它更简单，适用于大多数更新场景，而 PATCH 可能需要更多的处理逻辑来处理部分更新。

# 4. DELETE 是幂等的吗？为什么？
# - DELETE 是幂等的，因为无论你执行多少次 DELETE 请求，结果都是一样的：资源被删除了。如果资源已经被删除了，再次 DELETE 请求也不会改变结果。

# 5. HEAD 和 OPTIONS 是干什么的？（了解即可）
# - HEAD 请求资源的标头信息，而不返回资源的主体。OPTIONS 请求用于查询服务器支持哪些 HTTP 方法和功能。

# GET - 请求资源的时候
# POST - 创建资源的时候
# PUT - 更新资源的时候
# DELETE - 删除资源的时候
# PATCH - 更新部分资源的时候
# HEAD - 获取资源的元信息的时候
# OPTIONS - 查询服务器支持的 HTTP 方法和功能的时候

### HTTP 状态码
# 2xx - 成功
    # 200 OK - 请求成功，服务器返回了请求的数据
    # 201 Created - 请求成功，服务器创建了新的资源
    # 204 No Content - 请求成功，服务器没有返回任何内容（通常用于 DELETE 请求）
# 3xx - 重定向
    # 301 Moved Permanently - 请求的资源已经被永久移动到新的 URI
    # 302 Found - 请求的资源临时移动到新的 URI
    # 304 Not Modified - 请求的资源未被修改，可以使用缓存的版本
# 4xx - 客户端错误
    # 400 Bad Request - 请求无效，服务器无法理解
    # 401 Unauthorized - 请求未授权，客户端需要提供认证信息
    # 403 Forbidden - 请求授权了，但是没权限访问
    # 404 Not Found - 请求的资源未找到
    # 409 Conflict - 请求冲突，服务器无法完成请求
    # 422 Unprocessable Entity - 请求格式正确，但无法处理（通常用于验证错误）
# 5xx - 服务器错误
    # 500 Internal Server Error - 服务器内部错误，无法完成请求
    # 502 Bad Gateway - 服务器作为网关或代理时收到无效响应
    # 503 Service Unavailable - 服务器当前无法处理请求（过载或维护